<h2>How to use this tool:</h2>
<h3>Vocabulary by Chapter</h3>
<p>Record your vocabulary (nouns only) and their corresponding articles in this page. You can also record their plural forms too.</p>
<h3>Special verb forms</h3>
<p>Record verb forms (mostly present tense) in this specific order: ich, du, er/sie/es, wir, ihr, sie/Sie</p>
<h3>Grammar Express</h3>
<p>Attach a quick photo when you see a weird grammar rule.</p>

